    public void buscar(){
                // TODO add your handling code here:
        boolean login = this.logica.validarUsuario(this.txtUserName.getText(),this.txtPass.getText());
        UsuarioJpaController dao = new UsuarioJpaController();
        if(login){
            try{
               
                //MainFormOld ventanaSistema = new MainFormOld();
                Usuario user = this.logica.obtenerUsuario(this.txtUserName.getText());
                user.setTimestamp();
                MainMdi ventanaSistema = new MainMdi();
                ventanaSistema.inicializarVentanas(user);
                ventanaSistema.setLocationRelativeTo(null);
                //ventanaSistema.inicializarUsuario(user);
                
                dao.update(user, user.getId());
               // ventanaSistema.inicializaVentana();
                this.setVisible(false);
                ventanaSistema.setExtendedState(JFrame.MAXIMIZED_BOTH);
                ventanaSistema.setVisible(true);
                
            }catch(NonexistentEntityException ex){
                JOptionPane.showMessageDialog(null, "Error de Inicializacion.","LogIn", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            this.txtPass.setText(null);
            this.txtUserName.setText(null);
            JOptionPane.showMessageDialog(null, "Verifique los datos ingresados.","LogIn", JOptionPane.ERROR_MESSAGE);
        }
    }

